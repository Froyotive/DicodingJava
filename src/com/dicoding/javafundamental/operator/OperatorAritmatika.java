package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5+5;
        System.out.println("Hasil dari 5 + 5 = "+ hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 20-2;
        System.out.println("Hasil perkalian dari 20 - 2 = "+ hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Pengalian");
        int hasilPengalian = 6*7;
        System.out.println("Hasil Pengalian 6 * 7 = " + hasilPengalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 50/5;
        System.out.println("Hasil Pembagian 50 : 5 = " + hasilPembagian);
        System.out.println();

        System.out.println("Operasi Hasil Bagi");
        int hasilBagi1 = 10%2;
        System.out.println("Hasil 10 % 2 : "+ hasilBagi1);
        int hasilBagi2 = 11%2;
        System.out.println("Hasil 11 % 2 : "+ hasilBagi2);
    }
}
